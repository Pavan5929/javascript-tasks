// Declare a variable with a number 
var number = -1;  

// Check if the number is positive, negative, or zero using conditional statements
if (number > 0) {
    alert("The number is positive.");
} else if (number < 0) {
    alert("The number is negative.");
} else {
    alert("The number is zero.");
}
