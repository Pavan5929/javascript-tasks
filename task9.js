let grade = prompt("Please enter your grade (A, B, C, D, or F):");
//Display a message based on the grade entered
if (grade === "A") {
  alert("Excellent");
} else if (grade === "B") {
  alert("Good");
} else if (grade === "C") {
  alert("Average");
} else if (grade === "D") {
  alert("Poor");
} else if (grade === "F") {
  alert("Fail");
} else {
  alert("Invalid grade entered.");
}
