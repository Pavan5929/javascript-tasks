/*: Declare two variables with numeric values. Use the comparison operators to 
check if the first number is greater than, less than, or equal to the second number. Display 
the appropriate message using alert.*/

// Declare two variables with numeric values
var number1 = 10;
var number2 = 5;

// Compare the two numbers
if (number1 > number2) {
    alert(number1 + " is greater than " + number2);
} else if (number1 < number2) {
    alert(number1 + " is less than " + number2);
} else {
    alert(number1 + " is equal to " + number2);
}
