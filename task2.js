// Prompt 
let stringInput = prompt("Please enter a string:");

// Converting number to a string
let numberAsString = String(numberInput);

// Converting string to a number
let stringAsNumber = Number(stringInput);

// Display the original values
alert("Original number: " + numberInput);
alert("Original string: " + stringInput);

// Display the converted values
alert("Number as a string: " + numberAsString);
alert("String as a number: " + stringAsNumber);
