// Create a prompt to ask the user's age. If the user is 18 or older, display an alert saying "You are an adult." Otherwise, display an alert saying "You are a minor."
// Prompt the user to enter their age
var age = prompt("Please enter your age:");

// Check if the user is 18 or older
if (age >= 18) {
    // Display an alert saying the user is an adult
    alert("You are an adult.");
} else {
    // Display an alert saying the user is a minor
    alert("You are a minor.");
}